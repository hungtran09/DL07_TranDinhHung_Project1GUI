def process_postag_thesea(text):
    new_document = ''
    for sentence in sent_tokenize(text):
        sentence = sentence.replace('.','')
        ###### POS tag
        lst_word_type = ['N','Np','A','AB','V','VB','VY','R']
        # lst_word_type = ['A','AB','V','VB','VY','R']
        sentence = ' '.join( word[0] if word[1].upper() in lst_word_type else '' for word in pos_tag(process_special_word(word_tokenize(sentence, format="text"))))
        new_document = new_document + sentence + ' '
    ###### DEL excess blank space
    new_document = regex.sub(r'\s+', ' ', new_document).strip()
    return new_document

def remove_stopword(text, stopwords):
    ###### REMOVE stop words
    document = ' '.join('' if word in stopwords else word for word in text.split())
    #print(document)
    ###### DEL excess blank space
    document = regex.sub(r'\s+', ' ', document).strip()
    return document

def text_process_pipeline(x):
    def process_item(item):
        """Xử lý từng phần tử của dữ liệu."""
        item = process_text(item, emoji_dict, teen_dict, wrong_lst)
        item = covert_unicode(item)
        item = process_special_word(item)
        item = normalize_repeated_characters(item)
        item = process_postag_thesea(item)
        item = remove_stopword(item, stopwords_lst)
        return item

    if isinstance(x, np.ndarray):
        # Nếu đầu vào là một mảng NumPy
        return np.array([process_item(item) for item in x])
    elif isinstance(x, pd.Series):
        # Nếu đầu vào là một Series của pandas
        return x.apply(process_item)
    else:
        # Không hỗ trợ kiểu dữ liệu khác
        raise TypeError(f"Unsupported input type: {type(x)}")
